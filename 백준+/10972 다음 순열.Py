
n = int(input())
a = list(map(int, input().split()))

for i in range(n-1, 0, -1): #마지막 항부터 돈다
    if a[i-1] < a[i]: #만약 앞 열의 값이 뒷열의 값보다 작다면
        for j in range(n-1,0,-1): #다시 그 앞열의 값을 맨 뒷열부터 비교
            if a[i-1]<a[j]: #앞열의 값이 뒤에 있는 어느 열보다 작다면
                a[i-1], a[j] = a[j], a[i-1] #그 두 값을 스왑
                a = a[:i] + sorted(a[i:]) #i-1번째 까지의 리스트와 뒤에 리스트를 정렬한 채로 붙이기
                print(*a) # *를 이용해 리스트 내부의 원소들을 공백을 사용하여 출력
                exit() #코드 종료
print(-1) #위에서 코드 종료가 일어나지 않았다면(마지막 항이라면) -1 출력
                